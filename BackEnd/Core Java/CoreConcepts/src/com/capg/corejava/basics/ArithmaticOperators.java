package com.capg.corejava.basics;

public class ArithmaticOperators {

	public static void main(String[] args) {
		int i = 10;
		int j = 20;
		int res = i + j; // Addition
		System.out.println("Add is : " + res);
		res = j - i; // Substraction
		System.out.println("Sub is : " + res);
		res = i * j; // Multiplication
		System.out.println("Mul is : " + res);
		res = j / i; // Division
		System.out.println("Div is : " + res);
		res = i % j; // Modulus
		System.out.println("Mod id : " + res);
	}

}
