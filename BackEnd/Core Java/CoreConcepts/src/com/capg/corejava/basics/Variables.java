package com.capg.corejava.basics;

public class Variables {

	public static void main(String[] args) {
		byte b = 10;	//declaration and initialization of byte
	    short s = 101;	//declaration and initialization of short
		int i = 101;	//declaration and initialization of int
		float f = 101.01f;	//declaration and initialization of float #only 5 decimal digits
		long l = 10L;	//declaration and initialization of long #huge decimal digits
		double d = 123.456;	//declaration and initialization of double
		char c = 'A';	//declaration and initialization of char
		boolean q = true;	//declaration and initialization of boolean
		System.out.println(b);
		System.out.println(s);
		System.out.println(i);
		System.out.println(f);
		System.out.println(l);
		System.out.println(d);
		System.out.println(c);
		System.out.println(q);
	}

}
