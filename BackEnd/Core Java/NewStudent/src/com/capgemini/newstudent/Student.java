package com.capgemini.newstudent;

public class Student {

	int id;
	String name;
	char gender;
	double percentage;
	
	public Student() {
	}

	public Student(int id, String name, char gender, double percentage) {
		this.id = id;
		this.name = name;
		this.gender = gender;
		this.percentage = percentage;
	}
	
	
	
}
